<?php
echo "Таблица умножения от 1 до 10:\n\n";

// Внешний цикл для первого множителя (от 1 до 10)
for ($i = 1; $i <= 10; $i++) {
    // Внутренний цикл для второго множителя (от 1 до 10)
    for ($j = 1; $j <= 10; $j++) {
        // Используем printf для форматированного вывода
        printf("%2d x %2d = %3d\t", $i, $j, $i * $j);
    }
    echo "\n"; // Переход на новую строку после завершения внутреннего цикла
}